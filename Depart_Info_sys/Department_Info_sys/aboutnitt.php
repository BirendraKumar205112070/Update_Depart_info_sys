<p><font size="4">
The National Institute of Technology (formerly known as Regional Engineering College) Tiruchirappalli, situated in the heart of Tamil Nadu on the banks of river Cauvery, was started as a joint and co-operative venture of the Government of India and the Government of Tamil Nadu in 1964 with a view to catering to the needs of man-power in technology for the country. The college has been conferred with autonomy in financial and administrative matters to achieve rapid development. Because of this rich experience, this institution was granted Deemed University Status with the approval of the UGC/AICTE and Govt. of India in the year 2003 and renamed as National Institute of Technology. NIT-T was registered under Societies Registration Act XXVII of 1975.</font>
